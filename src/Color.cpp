#include "Color.h"

// ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: //

using namespace RT;

// ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: //

const Color Color::WHITE = Color( 1., 1., 1. );
const Color Color::BLACK = Color( 0., 0., 0. );
const Color Color::CHECKER = Color( -1., -1., -1. );

// ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: //

Color::Color( const double r,
              const double g,
              const double b,
              const double s )
: mR( r )
, mG( g )
, mB( b )
, mShininess( s )
{
}

// ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: //

Color::Color( void )
: mR( 0. )
, mG( 0. )
, mB( 0. )
, mShininess( 0.3 )
{
}

// ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: //

Color::~Color( void )
{
}

